"""Base class for Minion-based tests of evaluation metrics.

This module provides a base test class `MetricsTestBase` that simplifies
writing tests for evaluation metrics generated by a binary. It uses Minion
to compare the output metrics against a golden file.
"""

import os
import subprocess

from google3.ads.testing.minion.protos import minion_config_pb2
from google3.ads.testing.minion.public.python import minion
from google3.ads.testing.minion.public.python import options
from google3.net.proto2.python.public import text_format
from google3.pyglib import resources
from google3.testing.pybase import googletest

MINION_CONFIG_TEMPLATE = r"""
job_name: "%(job_name)s"
base_input {
  format: SINGLE_TEXT_PROTO
  locations: "%(golden_file_location)s"
  data_saved_as: TEXT
}
test_input {
  format: SINGLE_TEXT_PROTO
  locations: "%(metrics_file)s"
  data_saved_as: TEXT
}
proto_spec {
  proto_message_name: "accelerator_agents.evaluation.EvaluationResult"
}
golden_spec {
  golden_source: CODE_BASE
}
"""


class MetricsTestBase(googletest.TestCase):
  """Base class for testing metrics using Minion.

  Attributes:
    TEST_BINARY: The google3 path to the binary that generates the metrics.
      Subclasses must define this.
    GOLDEN_FILE_LOCATION: The google3 path to the Minion golden file. Subclasses
      must define this.
    JOB_NAME: A unique name for the Minion diff job. Subclasses must define
      this.
  """

  TEST_BINARY = None
  GOLDEN_FILE_LOCATION = None
  JOB_NAME = None

  def test_metrics(self):
    """Tests the metrics generated by TEST_BINARY against a golden file.

    This method runs the TEST_BINARY, captures its output metrics, and
    compares them against the GOLDEN_FILE_LOCATION using Minion.
    """
    if not self.TEST_BINARY:
      raise ValueError("TEST_BINARY must be set in subclass.")
    if not self.GOLDEN_FILE_LOCATION:
      raise ValueError("GOLDEN_FILE_LOCATION must be set in subclass.")
    if not self.JOB_NAME:
      raise ValueError("JOB_NAME must be set in subclass.")

    test_binary_path = resources.GetResourceFilename(self.TEST_BINARY)
    output_dir = self.create_tempdir()
    metrics_file = os.path.join(output_dir.full_path, "metrics.textproto")
    subprocess.run(
        [test_binary_path, f"--metrics_output_path={metrics_file}"], check=True
    )

    minion_obj = minion.Minion(options.LinkedProtoDB())
    config = MINION_CONFIG_TEMPLATE % {
        "job_name": self.JOB_NAME,
        "golden_file_location": self.GOLDEN_FILE_LOCATION,
        "metrics_file": metrics_file,
    }
    minion_config = text_format.Parse(
        config, minion_config_pb2.MinionDiffConfig()
    )

    self.assertEqual(
        minion_config_pb2.EQUAL,
        minion_obj.RunDiff(config=minion_config, test_case=self),
    )
